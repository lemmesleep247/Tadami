package tachiyomi.data.entries.anime

import aniyomi.domain.anime.SeasonAnime
import eu.kanade.tachiyomi.animesource.model.AnimeUpdateStrategy
import eu.kanade.tachiyomi.animesource.model.FetchType
import tachiyomi.domain.entries.anime.model.Anime
import tachiyomi.domain.library.anime.LibraryAnime
import tachiyomi.domain.source.anime.model.DeletableAnime

object AnimeMapper {
    fun mapAnime(
        id: Long,
        source: Long,
        url: String,
        artist: String?,
        author: String?,
        description: String?,
        notes: String,
        genre: List<String>?,
        title: String,
        status: Long,
        thumbnailUrl: String?,
        favorite: Boolean,
        lastUpdate: Long?,
        nextUpdate: Long?,
        initialized: Boolean,
        viewerFlags: Long,
        chapterFlags: Long,
        coverLastModified: Long,
        dateAdded: Long,
        updateStrategy: AnimeUpdateStrategy,
        calculateInterval: Long,
        lastModifiedAt: Long,
        favoriteModifiedAt: Long?,
        version: Long,
        @Suppress("UNUSED_PARAMETER")
        isSyncing: Long,
        fetchType: FetchType,
        parentId: Long?,
        seasonFlags: Long,
        seasonNumber: Double,
        seasonSourceOrder: Long,
        backgroundUrl: String?,
        backgroundLastModified: Long,
        pinned: Boolean,
        customTitle: String?,
        customArtist: String?,
        customAuthor: String?,
        customDescription: String?,
        customGenre: List<String>?,
        customStatus: Long?,
    ): Anime = Anime(
        id = id,
        source = source,
        favorite = favorite,
        pinned = pinned,
        lastUpdate = lastUpdate ?: 0,
        nextUpdate = nextUpdate ?: 0,
        fetchInterval = calculateInterval.toInt(),
        dateAdded = dateAdded,
        viewerFlags = viewerFlags,
        episodeFlags = chapterFlags,
        coverLastModified = coverLastModified,
        backgroundLastModified = backgroundLastModified,
        url = url,
        title = title,
        artist = artist,
        author = author,
        description = description,
        notes = notes,
        genre = genre,
        status = status,
        thumbnailUrl = thumbnailUrl,
        backgroundUrl = backgroundUrl,
        updateStrategy = updateStrategy,
        initialized = initialized,
        lastModifiedAt = lastModifiedAt,
        favoriteModifiedAt = favoriteModifiedAt,
        version = version,
        fetchType = fetchType,
        parentId = parentId,
        seasonFlags = seasonFlags,
        seasonNumber = seasonNumber,
        seasonSourceOrder = seasonSourceOrder,
        customTitle = customTitle,
        customArtist = customArtist,
        customAuthor = customAuthor,
        customDescription = customDescription,
        customGenre = customGenre,
        customStatus = customStatus,
    )

    fun mapLibraryAnime(
        id: Long,
        source: Long,
        url: String,
        artist: String?,
        author: String?,
        description: String?,
        notes: String,
        genre: List<String>?,
        title: String,
        status: Long,
        thumbnailUrl: String?,
        favorite: Boolean,
        lastUpdate: Long?,
        nextUpdate: Long?,
        initialized: Boolean,
        viewerFlags: Long,
        chapterFlags: Long,
        coverLastModified: Long,
        dateAdded: Long,
        updateStrategy: AnimeUpdateStrategy,
        calculateInterval: Long,
        lastModifiedAt: Long,
        favoriteModifiedAt: Long?,
        version: Long,
        isSyncing: Long,
        fetchType: FetchType,
        parentId: Long?,
        seasonFlags: Long,
        seasonNumber: Double,
        seasonSourceOrder: Long,
        backgroundUrl: String?,
        backgroundLastModified: Long,
        pinned: Boolean,
        customTitle: String?,
        customArtist: String?,
        customAuthor: String?,
        customDescription: String?,
        customGenre: List<String>?,
        customStatus: Long?,
        totalCount: Long,
        seenCount: Double,
        latestUpload: Long,
        episodeFetchedAt: Long,
        lastSeen: Long,
        bookmarkCount: Double,
        fillermarkCount: Double,
        category: Long,
    ): LibraryAnime = LibraryAnime(
        anime = mapAnime(
            id,
            source,
            url,
            artist,
            author,
            description,
            notes,
            genre,
            title,
            status,
            thumbnailUrl,
            favorite,
            lastUpdate,
            nextUpdate,
            initialized,
            viewerFlags,
            chapterFlags,
            coverLastModified,
            dateAdded,
            updateStrategy,
            calculateInterval,
            lastModifiedAt,
            favoriteModifiedAt,
            version,
            isSyncing,
            fetchType,
            parentId,
            seasonFlags,
            seasonNumber,
            seasonSourceOrder,
            backgroundUrl,
            backgroundLastModified,
            pinned,
            customTitle,
            customArtist,
            customAuthor,
            customDescription,
            customGenre,
            customStatus,
        ),
        category = category,
        totalCount = totalCount,
        seenCount = seenCount.toLong(),
        bookmarkCount = bookmarkCount.toLong(),
        fillermarkCount = fillermarkCount.toLong(),
        latestUpload = latestUpload,
        episodeFetchedAt = episodeFetchedAt,
        lastSeen = lastSeen,
    )

    fun mapSeasonAnime(
        id: Long,
        source: Long,
        url: String,
        artist: String?,
        author: String?,
        description: String?,
        notes: String,
        genre: List<String>?,
        title: String,
        status: Long,
        thumbnailUrl: String?,
        favorite: Boolean,
        lastUpdate: Long?,
        nextUpdate: Long?,
        initialized: Boolean,
        viewerFlags: Long,
        chapterFlags: Long,
        coverLastModified: Long,
        dateAdded: Long,
        updateStrategy: AnimeUpdateStrategy,
        calculateInterval: Long,
        lastModifiedAt: Long,
        favoriteModifiedAt: Long?,
        version: Long,
        isSyncing: Long,
        fetchType: FetchType,
        parentId: Long?,
        seasonFlags: Long,
        seasonNumber: Double,
        seasonSourceOrder: Long,
        backgroundUrl: String?,
        backgroundLastModified: Long,
        pinned: Boolean,
        customTitle: String?,
        customArtist: String?,
        customAuthor: String?,
        customDescription: String?,
        customGenre: List<String>?,
        customStatus: Long?,
        totalCount: Long,
        seenCount: Double,
        latestUpload: Long,
        fetchedAt: Long,
        lastSeen: Long,
        bookmarkCount: Double,
        fillermarkCount: Double,
    ): SeasonAnime = SeasonAnime(
        anime = mapAnime(
            id,
            source,
            url,
            artist,
            author,
            description,
            notes,
            genre,
            title,
            status,
            thumbnailUrl,
            favorite,
            lastUpdate,
            nextUpdate,
            initialized,
            viewerFlags,
            chapterFlags,
            coverLastModified,
            dateAdded,
            updateStrategy,
            calculateInterval,
            lastModifiedAt,
            favoriteModifiedAt,
            version,
            isSyncing,
            fetchType,
            parentId,
            seasonFlags,
            seasonNumber,
            seasonSourceOrder,
            backgroundUrl,
            backgroundLastModified,
            pinned,
            customTitle,
            customArtist,
            customAuthor,
            customDescription,
            customGenre,
            customStatus,
        ),
        totalCount = totalCount,
        seenCount = seenCount.toLong(),
        bookmarkCount = bookmarkCount.toLong(),
        fillermarkCount = fillermarkCount.toLong(),
        latestUpload = latestUpload,
        fetchedAt = fetchedAt,
        lastSeen = lastSeen,
    )

    fun mapDeletableAnime(
        id: Long,
        source: Long,
        fetchType: FetchType,
    ): DeletableAnime = DeletableAnime(
        animeId = id,
        sourceId = source,
        fetchType = fetchType,
    )
}
